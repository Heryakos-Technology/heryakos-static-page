  <script>
  import { defineComponent, onMounted } from 'vue';
  import { animate, spring, inView, delay } from 'motion';
  import TeamCard from '@/components/AboutUsComponents/TeamCard.vue'; 
  import { ref, onBeforeUnmount } from 'vue';
  import SocialIcon from '@/components/AboutUsComponents/SocialIcon.vue'; 
  
  export default defineComponent({
      components: {
          TeamCard,
          SocialIcon
      },
      setup() {
          const isVisible = ref(false); 
          const readmore = ref(false);
          const card = ref(null);
          let observer = null;
          const showContent = () => {
              readmore.value = !readmore.value;
          };
  
          const handleIntersect = (entries) => {
              entries.forEach(entry => {
                  if (entry.isIntersecting) {
                      isVisible.value = true; 
                  } else {
                      isVisible.value = false; 
                  }
              });
          };
  
       
          onMounted(() => {
              observer = new IntersectionObserver(handleIntersect);
              if (card.value) {
                  observer.observe(card.value);
              }
  
              setTimeout(() => {
                  isVisible.value = true; 
              }, 200); 
              onBeforeUnmount(() => {
              if (observer) {
                  observer.disconnect(); 
              }
          });
              const startAnimation = (element, animationProps) => {
                  animate(element, animationProps, {
                      duration: 3,
                      easing: [0.17, 0.55, 0.55, 1],
                      direction: 'alternate',
                    
                  });
              };
  
  
           
              inView(".box", (element, isInView) => {
                  if (isInView) {
                      startAnimation(element, { opacity: 1, x: [-100, 500] },{delay:100});
                  } else {
                   
                      animate(element, { opacity: 0, x: 0 });
                  }
              });
              inView(".smbox", (element, isInView) => {
                  if (isInView) {
                      startAnimation(element, { opacity: 1, x: [-100, 160] },{delay:100});
                  } else {
                   
                      animate(element, { opacity: 0, x: 0 });
                  }
              });
       
              inView(".box1", (element, isInView) => {
                  if (isInView) {
                      animate(element, { opacity: 0, x: 0 });
                      startAnimation(element, { opacity: 1, x: [0, -520] },{delay:100});
                  } else {
                   
                      animate(element, { opacity: 0, x: 0 });
                  }
              });
              inView(".smbox1", (element, isInView) => {
                  if (isInView) {
                      animate(element, { opacity: 0, x: 0 });
                      startAnimation(element, { opacity: 1, x: [0, -160] },{delay:100});
                  } else {
                   
                      animate(element, { opacity: 0, x: 0 });
                  }
              });
              inView(".smbox1", (element, isInView) => {
                  if (isInView) {
                      animate(element, { opacity: 0, x: 0 });
                      startAnimation(element, { opacity: 1, x: [0, -160] },{delay:100});
                  } else {
                   
                      animate(element, { opacity: 0, x: 0 });
                  }
              });
  
           
              inView(".box3", (element, isInView) => {
                  if (isInView) {
                      startAnimation(element, { scale: 1.1 },{delay:100});
                  } else {
                   
                      animate(element, { scale: 1 });
                  }
              });
              inView(".smbox3", (element, isInView) => {
                  if (isInView) {
                      startAnimation(element, { scale: 0.9 },{delay:100});
                  } else {
                   
                      animate(element, { scale: 1 });
                  }
              });
          });
      
          return {
              isVisible,
              readmore,
              showContent,
              card,
          };
      },
  });
